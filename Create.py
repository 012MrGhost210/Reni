        print(f"   - –ü–æ—Ä—Ç—Ñ–µ–ª–µ–π: {len(result.columns) - 1}")
        
        # –°—É–º–º–∞ –≤—Å–µ—Ö –ø–æ—Ä—Ç—Ñ–µ–ª–µ–π –∑–∞ –≤—Å–µ –¥–∞—Ç—ã
        portfolio_cols = [col for col in result.columns if col != 'Date']
