import pandas as pd

def transform_to_wide_format_clean(input_file_path, output_file_path):
    """–ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –¥–∞–Ω–Ω—ã–µ –≤ —à–∏—Ä–æ–∫–∏–π —Ñ–æ—Ä–º–∞—Ç —Å —á–∏—Å—Ç—ã–º–∏ –Ω–∞–∑–≤–∞–Ω–∏—è–º–∏ –ø–æ—Ä—Ç—Ñ–µ–ª–µ–π"""
    
    print(f"üîÑ –ü–†–ï–û–ë–†–ê–ó–û–í–ê–ù–ò–ï –í –ß–ò–°–¢–´–ô –®–ò–†–û–ö–ò–ô –§–û–†–ú–ê–¢...")
    
    try:
        # –ß–∏—Ç–∞–µ–º —Ñ–∞–π–ª
        df = pd.read_excel(input_file_path, header=0)
        df = df.rename(columns={df.columns[0]: '–ü–æ—Ä—Ç—Ñ–µ–ª—å'})
        
        # –ú–∞–ø–ø–∏–Ω–≥ –ø–æ—Ä—Ç—Ñ–µ–ª–µ–π
        portfolio_mapping = {
            '020611/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 02.06.2011': '020611/1',
            '020611/2 –∞–≥—Ä–µ—Å—Å. –æ—Ç 02.06.2011': '020611/2', 
            '020611/3 –∞–≥—Ä–µ—Å—Å. –æ—Ç 02.06.2011': '020611/3',
            '081121/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 08.11.2021': '081121/1',
            '081121/2 –∞–≥—Ä–µ—Å—Å. –æ—Ç 08.11.2021': '081121/2',
            '141111/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 14.11.2011': '141111/1',
            '190221/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 19.02.2021': '190221/1',
            '220223/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 22.02.2023': '220223/1',
            '220223/2 –∞–≥—Ä–µ—Å—Å. –æ—Ç 22.02.2023': '220223/2',
            '260716/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 26.07.2016': '260716/1',
            '271210/2 –∞–≥—Ä–µ—Å—Å. –æ—Ç 27.12.2010': '271210/2',
            '050925/1 –∞–≥—Ä–µ—Å—Å. –æ—Ç 05.09.2025': '050925/1'
        }
        
        # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –Ω—É–∂–Ω—ã–µ –∫–æ–ª–æ–Ω–∫–∏
        money_columns = [
            '–°—Ç–æ–∏–º–æ—Å—Ç—å',
            '–ù–ö–î,\n–Ω–∞—á–∏—Å–ª–µ–Ω–Ω—ã–µ %', 
            '–î–µ–±–µ—Ç–æ—Ä—Å–∫–∞—è/ –ö—Ä–µ–¥–∏—Ç–æ—Ä—Å–∫–∞—è –∑–∞–¥–æ–ª–∂–µ–Ω–Ω–æ—Å—Ç–∏'
        ]
        
        # –ù–∞—Ö–æ–¥–∏–º –∫–æ–ª–æ–Ω–∫—É —Å –¥–∞—Ç–æ–π –æ—Ç—á–µ—Ç–∞
        date_column = None
        for col in df.columns:
            if '–¥–∞—Ç–∞' in str(col).lower() and '–æ—Ç—á–µ—Ç' in str(col).lower():
                date_column = col
                break
        
        if date_column is None:
            print("‚ùå –ù–µ –Ω–∞–π–¥–µ–Ω–∞ –∫–æ–ª–æ–Ω–∫–∞ —Å –¥–∞—Ç–æ–π –æ—Ç—á–µ—Ç–∞")
            return None
        
        print(f"–ö–æ–ª–æ–Ω–∫–∞ —Å –¥–∞—Ç–æ–π: '{date_column}'")
        
        # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –¥–∞—Ç—É –∏ —á–∏—Å–ª–æ–≤—ã–µ –∫–æ–ª–æ–Ω–∫–∏
        df[date_column] = pd.to_datetime(df[date_column], errors='coerce')
        
        for col in money_columns:
            if col in df.columns:
                df[col] = pd.to_numeric(df[col], errors='coerce').fillna(0)
        
        # –§–∏–ª—å—Ç—Ä—É–µ–º –≤–∞–ª–∏–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
        df = df[df['–ü–æ—Ä—Ç—Ñ–µ–ª—å'].notna()]
        df = df[df[date_column].notna()]
        
        # –ü—Ä–∏–º–µ–Ω—è–µ–º –º–∞–ø–ø–∏–Ω–≥ –∫ –ø–æ—Ä—Ç—Ñ–µ–ª—è–º
        df['–ü–æ—Ä—Ç—Ñ–µ–ª—å_—á–∏—Å—Ç—ã–π'] = df['–ü–æ—Ä—Ç—Ñ–µ–ª—å'].map(portfolio_mapping)
        
        # –§–∏–ª—å—Ç—Ä—É–µ–º —Ç–æ–ª—å–∫–æ –ø–æ—Ä—Ç—Ñ–µ–ª–∏ –∏–∑ –º–∞–ø–ø–∏–Ω–≥–∞
        df_clean = df[df['–ü–æ—Ä—Ç—Ñ–µ–ª—å_—á–∏—Å—Ç—ã–π'].notna()].copy()
        
        print(f"‚úÖ –ü–æ—Å–ª–µ —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ –æ—Å—Ç–∞–ª–æ—Å—å –ø–æ—Ä—Ç—Ñ–µ–ª–µ–π: {df_clean['–ü–æ—Ä—Ç—Ñ–µ–ª—å_—á–∏—Å—Ç—ã–π'].nunique()}")
        print(f"üìä –£–Ω–∏–∫–∞–ª—å–Ω—ã–µ –ø–æ—Ä—Ç—Ñ–µ–ª–∏: {df_clean['–ü–æ—Ä—Ç—Ñ–µ–ª—å_—á–∏—Å—Ç—ã–π'].unique().tolist()}")
        
        # –°—É–º–º–∏—Ä—É–µ–º –Ω—É–∂–Ω—ã–µ –∫–æ–ª–æ–Ω–∫–∏
        df_clean['–û–±—â–∞—è_—Å—É–º–º–∞'] = 0
        for col in money_columns:
            if col in df_clean.columns:
                df_clean['–û–±—â–∞—è_—Å—É–º–º–∞'] += df_clean[col]
        
        # –ì—Ä—É–ø–ø–∏—Ä—É–µ–º –ø–æ –¥–∞—Ç–µ –∏ —á–∏—Å—Ç–æ–º—É –ø–æ—Ä—Ç—Ñ–µ–ª—é
        grouped = df_clean.groupby([date_column, '–ü–æ—Ä—Ç—Ñ–µ–ª—å_—á–∏—Å—Ç—ã–π'])['–û–±—â–∞—è_—Å—É–º–º–∞'].sum().reset_index()
        
        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —à–∏—Ä–æ–∫–∏–π —Ñ–æ—Ä–º–∞—Ç (pivot)
        wide_df = grouped.pivot_table(
            index=date_column,
            columns='–ü–æ—Ä—Ç—Ñ–µ–ª—å_—á–∏—Å—Ç—ã–π',
            values='–û–±—â–∞—è_—Å—É–º–º–∞',
            aggfunc='sum'
        ).reset_index()
        
        # –ó–∞–ø–æ–ª–Ω—è–µ–º –ø—Ä–æ–ø—É—â–µ–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –Ω—É–ª—è–º–∏
        wide_df = wide_df.fillna(0)
        
        # –ü–µ—Ä–µ–∏–º–µ–Ω–æ–≤—ã–≤–∞–µ–º –∫–æ–ª–æ–Ω–∫—É —Å –¥–∞—Ç–æ–π –∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –¥–∞—Ç—ã
        wide_df = wide_df.rename(columns={date_column: 'Date'})
        wide_df['Date'] = wide_df['Date'].dt.strftime('%d.%m.%Y')
        
        # –£–ø–æ—Ä—è–¥–æ—á–∏–≤–∞–µ–º –∫–æ–ª–æ–Ω–∫–∏ —Å–æ–≥–ª–∞—Å–Ω–æ –º–∞–ø–ø–∏–Ω–≥—É
        ordered_columns = ['Date'] + list(portfolio_mapping.values())
        # –û—Å—Ç–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –∫–æ–ª–æ–Ω–∫–∏
        final_columns = [col for col in ordered_columns if col in wide_df.columns]
        wide_df = wide_df[final_columns]
        
        print(f"‚úÖ –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–æ –≤ —á–∏—Å—Ç—ã–π —à–∏—Ä–æ–∫–∏–π —Ñ–æ—Ä–º–∞—Ç:")
        print(f"   - –î–∞—Ç: {len(wide_df)}")
        print(f"   - –ü–æ—Ä—Ç—Ñ–µ–ª–µ–π: {len(wide_df.columns) - 1}")  # –º–∏–Ω—É—Å –∫–æ–ª–æ–Ω–∫–∞ Date
        print(f"   - –û–±—â–∞—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∞: {wide_df.shape}")
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤—ã–µ –Ω–µ—Å–∫–æ–ª—å–∫–æ —Å—Ç—Ä–æ–∫
        print(f"\nüìä –ü–†–ï–í–¨–Æ –î–ê–ù–ù–´–•:")
        print(wide_df.head())
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ Excel
        wide_df.to_excel(output_file_path, index=False)
        print(f"\nüíæ –§–∞–π–ª —Å–æ—Ö—Ä–∞–Ω–µ–Ω: {output_file_path}")
        
        # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è
        print(f"\nüìà –°–í–û–î–ö–ê:")
        for portfolio in portfolio_mapping.values():
            if portfolio in wide_df.columns:
                total = wide_df[portfolio].sum()
                print(f"   {portfolio}: {total:,.2f}")
        
        return wide_df
        
    except Exception as e:
        print(f"‚ùå –û—à–∏–±–∫–∞: {e}")
        import traceback
        traceback.print_exc()
        return None

